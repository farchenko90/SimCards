    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(tipo.equals("datos")){
            d.setNumero(txtnumero.getText());
            d.setValor(Double.parseDouble(txtvalor.getText()));
            d.setGb_Mb(txtmindatos.getText());
            boolean val = dControl.modificardatos(d, x);
            if(val){
                dControl.ListaDatos("", tablasim.jTable1);
                JOptionPane.showMessageDialog(null, "Se ha modificado este registro");
                
                this.dispose();
            }
        }else{
            p.setNumero(txtnumero.getText());
            p.setValor(Double.parseDouble(txtvalor.getText()));
            p.setNum_minutos(txtmindatos.getText());
            boolean acep = pControl.ModificarPrepago(p, x);
            if(acep){
                dControl.ListaDatos("", tablasim.jTable1);
                JOptionPane.showMessageDialog(null, "Se ha modificado este registro");
                
                this.dispose();
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

