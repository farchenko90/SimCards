    public void cargar(){
        System.out.println(x);
        if(tipo.equals("datos")){
            arraydatos = dControl.unasimcards(d, x);
            jLabel4.setText("Cantidad Datos");
              
            for(Datos arraydato: arraydatos){
                txtnumero.setText(arraydato.getNumero());
                txtvalor.setText(String.valueOf(arraydato.getValor()));
                txtmindatos.setText(arraydato.getGb_Mb());
            }
        }else{
            arrayprepago = pControl.unasimcards(p, x);
            jLabel4.setText("Cantidad Minutos");
            for(Prepago arrayPrepago: arrayprepago){
                txtnumero.setText(arrayPrepago.getNumero());
                txtvalor.setText(String.valueOf(arrayPrepago.getValor()));
                txtmindatos.setText(arrayPrepago.getNum_minutos());
            }
        }
    }
    
    private void sombra_total_panel(JPanel jp){
        DropShadowBorder shadow = new DropShadowBorder();
        shadow.setShadowColor(Color.BLACK);
        shadow.setShowLeftShadow(false);
        shadow.setShowRightShadow(false);
        shadow.setShowBottomShadow(true);
        shadow.setShowTopShadow(false);
        shadow.setShadowSize(7);
        shadow.setShadowOpacity(0.3f);
        jp.setBorder(shadow);
    }
    
